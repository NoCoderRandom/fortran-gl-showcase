def glyph_rows(ch: str) -> list[int]:
    pattern = FONT.get(ch, FONT["?"])
    rows = [0] * 8
    for y, line in enumerate(pattern, start=0):
        bits = 0
        for x, pixel in enumerate(line):
            if pixel == "#":
                bits |= 1 << (7 - (x + 1))
        rows[y] = bits
    return rows


def emit() -> str:
    rows = []
    for code in range(128):
        ch = chr(code)
        if "a" <= ch <= "z":
            ch = ch.upper()
        rows.extend(glyph_rows(ch))

    chunks = []
    for start in range(0, len(rows), 8):
        chunk = ", ".join(f"{value}_c_int8_t" for value in rows[start:start + 8])
        chunks.append(f"    & {chunk}")
    values = ", &\n".join(chunks)
    return f"""module render_font_data
  use, intrinsic :: iso_c_binding, only: c_int8_t
  implicit none (type, external)
  private

  integer, parameter, public :: font_count = 128
  integer, parameter, public :: font_width = 8
  integer, parameter, public :: font_height = 8
  integer(c_int8_t), parameter, public :: font_rows(font_height, font_count) = reshape([ &
{values} &
  ], [font_height, font_count])
end module render_font_data
"""


def main() -> None:
    out = Path("src/render/font_data.f90")
    out.write_text(emit(), encoding="ascii")


if __name__ == "__main__":
    main()
